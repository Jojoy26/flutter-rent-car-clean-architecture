class CarModel {
  final String carName;
  final String refDocument;
  final String brand;
  final String img;
  final int price;
  final String rentBy;
  final int reviewsNumber;
  final double score;
  final String locationCar;
  final String description;
  final List<dynamic> specifications;
  CarModel({
    required this.carName,
    required this.refDocument,
    required this.brand,
    required this.img,
    required this.price,
    required this.rentBy,
    required this.reviewsNumber,
    required this.score,
    required this.locationCar,
    required this.description,
    required this.specifications,
  });
  
}
